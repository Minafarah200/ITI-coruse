#include <iostream>
using namespace std;

int main() {
    int n = 7; // ارتفاع حرف M (غير الرقم لتكبير أو تصغير الحرف)

    for (int i = 0; i < n; i++) {
        // العمود الأول
        cout << "*";

        // مسافات قبل الخط المائل الأول
        for (int j = 0; j < i; j++) cout << " ";

        // النجمة الداخلية (الخط المائل الأول)
        cout << "*";

        // مسافات بين الخطين المائلين
        for (int j = 0; j < (n - i - 1) * 2 - 1; j++) cout << " ";

        // النجمة الداخلية (الخط المائل الثاني) – ما تظهرش في آخر سطر
        if (i != n - 1) cout << "*";

        // مسافات بعد الخط المائل الثاني
        for (int j = 0; j < i; j++) cout << " ";

        // العمود الأخير
        cout << "*" << endl;
    }

    return 0;
}
